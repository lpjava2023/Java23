package com.itheima.operator;
//关系运算符结果为boolean

public class OperatorDemo3 {
    public static void main(String[] args) {
        System.out.println(10>20);
        System.out.println(10>=20);
        System.out.println(10<=20);
        System.out.println(10==20);//判断是否相等
        System.out.println(10!=20);
        System.out.println();
    }
}
